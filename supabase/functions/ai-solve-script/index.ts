import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { host, port, interactions, interaction_log, job_id, challenge_type } = await req.json();
    
    const LOVABLE_API_KEY = Deno.env.get('LOVABLE_API_KEY');
    if (!LOVABLE_API_KEY) {
      throw new Error('LOVABLE_API_KEY is not configured');
    }

    console.log('[ai-solve-script] Generating solve script for:', { host, port, job_id });

    const systemPrompt = `You are an expert CTF player and exploit developer. Your task is to generate a Python solve script using pwntools based on the interaction log provided.

Guidelines:
- Use pwntools (from pwn import *)
- Include proper error handling
- Add comments explaining each step
- Extract and print any flags found
- Handle common CTF patterns (buffer overflows, format strings, crypto challenges)
- Make the script reusable and well-documented

Output ONLY the Python script, no explanations before or after.`;

    const userPrompt = `Generate a pwntools solve script for this CTF challenge:

Target: ${host}:${port}
Challenge Type: ${challenge_type || 'unknown'}

Interaction Log:
${interaction_log || interactions?.join('\n') || 'No interactions recorded'}

User Inputs Sent:
${interactions?.map((i: string, idx: number) => `${idx + 1}. "${i}"`).join('\n') || 'None'}

Create a complete, working solve script that:
1. Connects to the target
2. Handles the challenge interaction
3. Extracts and displays the flag
4. Includes error handling`;

    const response = await fetch('https://ai.gateway.lovable.dev/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${LOVABLE_API_KEY}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'google/gemini-2.5-flash',
        messages: [
          { role: 'system', content: systemPrompt },
          { role: 'user', content: userPrompt }
        ],
        max_tokens: 2000,
        temperature: 0.3,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error('[ai-solve-script] AI API error:', response.status, errorText);
      
      if (response.status === 429) {
        return new Response(JSON.stringify({ 
          error: 'Rate limit exceeded. Please try again later.',
          script: generateFallbackScript(host, port, interactions)
        }), {
          status: 429,
          headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        });
      }
      
      if (response.status === 402) {
        return new Response(JSON.stringify({ 
          error: 'Payment required. Please add credits to your workspace.',
          script: generateFallbackScript(host, port, interactions)
        }), {
          status: 402,
          headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        });
      }
      
      throw new Error(`AI API error: ${response.status}`);
    }

    const data = await response.json();
    let script = data.choices?.[0]?.message?.content || '';
    
    // Clean up the script (remove markdown code blocks if present)
    script = script.replace(/^```python\n?/m, '').replace(/^```\n?/m, '').replace(/```$/m, '').trim();
    
    // Add shebang if missing
    if (!script.startsWith('#!')) {
      script = `#!/usr/bin/env python3\n${script}`;
    }

    console.log('[ai-solve-script] Generated script length:', script.length);

    return new Response(JSON.stringify({
      script,
      host,
      port,
      generated_at: new Date().toISOString(),
    }), {
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    });

  } catch (error) {
    console.error('[ai-solve-script] Error:', error);
    
    // Return fallback script on error - use default values since body was already consumed
    return new Response(JSON.stringify({ 
      error: error instanceof Error ? error.message : 'Unknown error',
      script: generateFallbackScript('localhost', '9999', [])
    }), {
      status: 500,
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    });
  }
});

function generateFallbackScript(host: string, port: string, interactions: string[]): string {
  return `#!/usr/bin/env python3
"""
CTF Netcat Solve Script
Target: ${host}:${port}
Auto-generated by CTF Autopilot
"""
from pwn import *

# Configuration
HOST = "${host}"
PORT = ${port || 9999}
context.log_level = 'info'

def solve():
    """Main solve function"""
    try:
        # Connect to the target
        r = remote(HOST, PORT)
        log.info(f"Connected to {HOST}:{PORT}")
        
        # Receive initial banner
        banner = r.recvuntil(b":", timeout=5)
        log.info(f"Banner: {banner.decode()}")
        
${(interactions || []).map((cmd: string, i: number) => `        # Step ${i + 1}: Send "${cmd}"
        r.sendline(b"${cmd.replace(/"/g, '\\"')}")
        response_${i} = r.recvline(timeout=5)
        log.info(f"Response ${i + 1}: {response_${i}.decode()}")
`).join('\n') || '        # No recorded interactions\n        # Add your exploit code here\n        pass'}
        
        # Try to receive flag
        try:
            remaining = r.recvall(timeout=3)
            log.success(f"Received: {remaining.decode()}")
            
            # Extract flag
            import re
            flags = re.findall(rb'[A-Z]{2,5}\\{[^}]+\\}', remaining)
            if flags:
                log.success(f"FLAG: {flags[0].decode()}")
        except:
            pass
        
        r.close()
        
    except Exception as e:
        log.error(f"Error: {e}")
        return False
    
    return True

if __name__ == "__main__":
    solve()
`;
}
